import { checkExceptions, createWaiter, WaiterState, } from "@smithy/util-waiter";
import { GetMemoryCommand } from "../commands/GetMemoryCommand";
const checkState = async (client, input) => {
    let reason;
    try {
        let result = await client.send(new GetMemoryCommand(input));
        reason = result;
        try {
            const returnComparator = () => {
                return result.memory.status;
            };
            if (returnComparator() === "CREATING") {
                return { state: WaiterState.RETRY, reason };
            }
        }
        catch (e) { }
        try {
            const returnComparator = () => {
                return result.memory.status;
            };
            if (returnComparator() === "ACTIVE") {
                return { state: WaiterState.SUCCESS, reason };
            }
        }
        catch (e) { }
        try {
            const returnComparator = () => {
                return result.memory.status;
            };
            if (returnComparator() === "FAILED") {
                return { state: WaiterState.FAILURE, reason };
            }
        }
        catch (e) { }
    }
    catch (exception) {
        reason = exception;
    }
    return { state: WaiterState.RETRY, reason };
};
export const waitForMemoryCreated = async (params, input) => {
    const serviceDefaults = { minDelay: 2, maxDelay: 120 };
    return createWaiter({ ...serviceDefaults, ...params }, input, checkState);
};
export const waitUntilMemoryCreated = async (params, input) => {
    const serviceDefaults = { minDelay: 2, maxDelay: 120 };
    const result = await createWaiter({ ...serviceDefaults, ...params }, input, checkState);
    return checkExceptions(result);
};
